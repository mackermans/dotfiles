-- Fixes an issue with Telescope where you're stuck in insert mode after opening a file buffer with Telescope
-- https://github.com/nvim-telescope/telescope.nvim/issues/2501#issuecomment-1561838990
vim.api.nvim_create_autocmd("WinLeave", {
  callback = function()
    if vim.bo.ft == "TelescopePrompt" and vim.fn.mode() == "i" then
      vim.api.nvim_feedkeys(vim.api.nvim_replace_termcodes("<Esc>", true, false, true), "i", false)
    end
  end,
})
